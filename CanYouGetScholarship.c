#include <stdio.h>

int main(void)
{
	int a;
	scanf_s("%d", &a);
	if (a <= 100000) printf("%.2f\n", a * 0.1);
	else if (a <= 200000) printf("%.2f\n", 100000 * 0.1 + (a - 100000) * 0.075);
	else if (a <= 400000) printf("%.2f\n",
		100000 * 0.1 + 100000 * 0.075 + (a - 200000) * 0.05);
	else if (a <= 600000) printf("%.2f\n",
		100000 * 0.1 + 100000 * 0.075 + 200000 * 0.05 + (a - 400000) * 0.03);
	else if (a <= 1000000) printf("%.2f\n",
		100000 * 0.1 + 100000 * 0.075 + 200000 * 0.05 + 200000 * 0.03 + (a - 600000) * 0.015);
	else printf("%.2f\n", 
		100000 * 0.1 + 100000 * 0.075 + 200000 * 0.05 + 200000 * 0.03 + 400000 * 0.015 + (a - 1000000) * 0.01);
	system("pause");
}//终极懒惰就是这样吧，看到有同学一行?:写完，好强（虽然不要学他）
